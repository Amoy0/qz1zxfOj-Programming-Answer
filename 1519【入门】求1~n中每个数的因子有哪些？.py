# 描述
# 输出1~n中每个数的因子有哪些，从小到大输出！
# 比如：n=10，那么输出结果如下。
# 1:1
# 2:1 2
# 3:1 3
# 4:1 2 4
# 5:1 5
# 6:1 2 3 6
# 7:1 7
# 8:1 2 4 8
# 9:1 3 9
# 10:1 2 5 10
# 其中，每行冒号前的整数代表了1~n的每个数，冒号后的的数字代表了这个整数有哪些因子，用空格隔开。

# 输入
# 一个整数n（n<=1000）

# 输出
# 按要求输出1~n中每个数的因子有哪些。
n=int(input())
i=1
a=1
while i<=n:
    print(i,end="")
    print(":",end="")
    while a<=i:
        b=i%a
        if b==0:
            print(a,end=" ")
        a=a+1
    i=i+1
    a=1
    print()
