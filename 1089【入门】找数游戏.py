# 描述
# 一个三位数，各位数字互不相同，十位数字比个位、百位数字之和还要大，且十位、百位数字之和不是质数。
# 桐桐想把符合上述条件的三位数找出来，你能帮助她吗？

# 输入
# 无

# 输出
# 按照从小到大的顺序，输出满足条件的三位数，每行一个。
for a in range(100,1000): 
    bool=0
    a1=a//100
    a2=(a-100*a1)//10
    a3=a-100*a1-10*a2
    for i in range(2,(a1+a2)):
        if (a1+a2)%i==0:
            bool=1
            break
    if bool==1 and a2>a1+a3 and a1!=a2 and a1!=a3 and a2!=a3:
        print(a)

